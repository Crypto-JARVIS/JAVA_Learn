package house;

public class Celling
{
    private int height;
    private String paintedcolor;

    public Celling(int height, String paintedcolor)
    {
        this.height = height;
        this.paintedcolor = paintedcolor;
    }

    public int getHeight()
    {
        return height;
    }
    public String getPaintedcolor()
    {
        return paintedcolor;
    }
}